                        <h4>汽車底盤防護</h4>
                        <p class="segmented">
                            1. BD 47/94 防水測試<br />
                            BD 47/94是英國橋面板防水的測試標準，裡面有一項是測試防水膜抗裂的，在25攝氏度下，以1個循環/秒的速率在規定的裂縫寬度，進行總共100個循環。在測試期間的任何階段，防水膜都不能有裂縫產生。
                        </p>
                        <p class="segmented">
                            2. 樹根穿透測試 din en 13948<br />
                            DIN EN 13948的樹根穿刺測試標準是用火棘這種植物來做測試，大陸工程要求除了火棘之外還要加做榕樹或綠竹測試，而且測試溫度要依照台灣四季的自然天候溫度，其他就參照DIN EN 13948的方法來測試，經過3年的測試之後，開槽檢視無任何拜鐵膜被樹根穿刺過，拜鐵膜再檢測其物性並無降低或損耗。
                        </p>
                        <p class="segmented">
                            3. BS 6920 飲用水測試<br />
                            是英國的人體接觸飲用水不影響水質安全測試標準，台灣測試標準是測八大重金屬及濁度及氣味，而BS 6920除了測試上述項目之外，還要用猴子的腎臟細胞來測試，因為全世界的毒害化學品每日都有新產品，無法用質譜來比對，只能用猴子的腎臟細胞來代替人，要測試比對沒有反應才能表示此產品對人體沒有危害，這是比台灣的測試標準更嚴謹。
                        </p>
                        <p class="segmented">
                            4. 200萬道渣測試<br />
                            是歐洲鐵路對橋面板防水很嚴苛的測試項目之一，將拜鐵膜上面直接置放標準道渣(碎石有銳利角)，用 41 to 125kN 施壓在道渣上面，由道渣將壓力傳遞給拜鐵膜，經過2百萬次模擬火車經過反覆施壓，拜鐵膜沒有被刺破，再將拜鐵膜放置在5 bar水壓下，置放24小時後，沒有任何漏水，才能通過此項測試。
                        </p>
